<script>
	let count = $state(0);

	function increment() {
		count += 1;
	}

	function decrement() {
		if (count < 1) return;
		count -= 1;
	}
</script>

<p>Contador: {count}</p>

<button onclick={increment}>Incrementar +1</button>

<button onclick={decrement}>Decrementar -1</button>
