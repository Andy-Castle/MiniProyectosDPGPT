<script>
	import Saludo from './Saludo.svelte';

	const nombres = ['Andy', 'Frida', 'Pamela', 'Jessica', 'Pedro', 'Yesenia'];

	let persona = $state('Jaime');

	function randomName() {
		let numeroRandom = Math.floor(Math.random() * nombres.length);

		return nombres[numeroRandom];
	}

	setInterval(() => {
		persona = randomName();
	}, 3000);
</script>

<Saludo name={persona} />
